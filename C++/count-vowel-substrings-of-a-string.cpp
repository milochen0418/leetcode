    int countVowelSubstrings(string word) {
        int ans = 0;
        int n = word.size();
        for(int i = 0; i<n;i++) {
            unordered_set<char> s;
            for(int j = i;j<n;j++) {
                if(isVowels[word[j]]) {
                    s.insert(word[j]);
                } else {
                    break;
                }
                if(s.size() >= 5) ans++;
            }
        }
        return ans;
    }
